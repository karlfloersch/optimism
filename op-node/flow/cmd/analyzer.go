func main() {
	if len(os.Args) < 2 {
		fmt.Println("Usage: analyzer <trace-file.json> [options]")
		fmt.Println("       analyzer --help")
		os.Exit(1)
	}

	arg := os.Args[1]
	if arg == "--help" || arg == "-h" {
		printHelp()
		return
	}

	filename := arg
	if err := analyzeTrace(filename); err != nil {
		fmt.Printf("Error analyzing trace: %v\n", err)
		os.Exit(1)
	}
}

